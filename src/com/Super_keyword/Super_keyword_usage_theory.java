package com.Super_keyword;

public class Super_keyword_usage_theory {
	
	/*
	 >	With Variable:
			Scenario--When parent class and child class has the same variable with same name and 
					  if child class want to access the parent class variable, in that case we will 
					  use super	keyword.

	>	With Method:
			Scenario--When parent clas and child class has the same method name and if you want to 
					  call parent class method, in that case we will use super keyword.

	>	With Constructor:
			Scenario--Whenever we need to call base class constructor from child class, we can use 
					  super() keyword .
	 */

}
